// Program to sum three variables and output the result

public class SumThreeVariables {
	public static void main(String [] args)
	{
		// Variable declaration and assignment
		int a = 5;
		int b = 11;
		int c = 23;
	
		// Computer sum of the three variables and assign to new variable
		
		int sum = a + b + c;
		
		System.out.println();
		System.out.print("The variable sum contains the value " + sum + ".");
		
		System.exit(0);
		
	}
	
}
